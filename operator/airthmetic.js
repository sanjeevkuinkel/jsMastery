//!ALL THESE OPERATION REPLACE THE MAIN VARIABLE
//?addition
let a = 1 + 2;
//?subtraction
let b = 3 - 4;
//?multiplication
let c = 5 * 6;
//?division
let d = 7 / 1;
//?modulus
let e = 8 % 2;
//?post increment
let f = d++; //output:7
console.log(d); //output:8
//?pre increment
let g = ++d; //output:9
//?post decrement
let h = d--; //output:9
console.log(d); //output:8
//?pre decrement
let i = --d; //output;7
//exponential
let j = 5 ** 2;
console.log(`addition:`, a);
console.log(`subtraction:`, b);
console.log(`multiplication:`, c);
console.log(`division:`, d);
console.log(`modulus:`, e);
console.log(`post increment:`, f);
console.log(`pre increment:`, g);
console.log(`post decrement:`, h);
console.log(`pre decrement:`, i);
console.log(`exponential:`, j);
